import fs from "node:fs";
import path from "node:path";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const args = new Map();
for (let i = 2; i < process.argv.length; i += 1) {
  const arg = process.argv[i];
  if (arg.startsWith("--")) {
    args.set(arg.slice(2), process.argv[i + 1]);
    i += 1;
  }
}

const packageJson = JSON.parse(fs.readFileSync(path.join(repoRoot, "package.json"), "utf8"));
const version = String(args.get("version") ?? process.env.GITHUB_REF_NAME ?? `v${packageJson.version}`);
const normalizedVersion = version.startsWith("v") ? version : `v${version}`;
const outputPath = path.resolve(repoRoot, String(args.get("out") ?? path.join("artifacts", `CHANGELOG-${normalizedVersion}.md`)));
fs.mkdirSync(path.dirname(outputPath), { recursive: true });

const previousTag = findPreviousTag(normalizedVersion);
const range = previousTag ? `${previousTag}..HEAD` : "HEAD";
const commits = git(["log", range, "--pretty=format:%h%x09%s"])
  .split("\n")
  .map((line) => line.trim())
  .filter(Boolean)
  .map((line) => {
    const [hash, ...subjectParts] = line.split("\t");
    return { hash, subject: subjectParts.join("\t") };
  });

const lines = [
  `# ${normalizedVersion}`,
  "",
  previousTag ? `Changes since ${previousTag}.` : "Initial release notes.",
  "",
  "## Changes",
  ""
];

if (commits.length === 0) {
  lines.push("- No commits found in the release range.");
} else {
  for (const commit of commits) {
    lines.push(`- ${commit.subject} (${commit.hash})`);
  }
}

lines.push("");
lines.push("## Verification");
lines.push("");
lines.push("- CI runs `npm test` before publishing this release artifact.");
lines.push("- Release artifacts include the npm package tarball generated by `npm pack`.");
lines.push("");

fs.writeFileSync(outputPath, `${lines.join("\n")}\n`);
console.log(outputPath);

function findPreviousTag(currentTag) {
  const tags = git(["tag", "--sort=-creatordate"])
    .split("\n")
    .map((tag) => tag.trim())
    .filter(Boolean)
    .filter((tag) => tag !== currentTag);
  return tags[0] ?? "";
}

function git(args) {
  try {
    return execFileSync("git", args, { cwd: repoRoot, encoding: "utf8" }).trim();
  } catch {
    return "";
  }
}
